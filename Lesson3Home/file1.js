/* Завдання 1
Написати програму, яка визначає, чи є введене число парним або непарним. */

const number = parseInt(prompt("Введіть число: "));
if (number % 2 === 0) {
    console.log(" Число парне ");
} else {
    console.log(" Число непарне ");
}

