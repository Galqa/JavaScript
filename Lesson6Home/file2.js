/* Завдання 2
Написати програму, яка працює зі списком покупок: додає нові товари, видаляє наявні та виводить список товарів на екран.

Кроки для виконання завдання:
1. Створити масив для зберігання списку покупок:
   - Початково масив може бути порожнім або містити кілька базових товарів.
2. Створити функцію для додавання товару до списку:
   - Функція приймає назву товару як аргумент і додає його до масиву.
3. Створити функцію для видалення товару зі списку:
   - Функція приймає назву товару як аргумент, знаходить його у масиві та видаляє.
4. Створити функцію для виведення поточного списку покупок:
   - Функція виводить всі елементи масиву у зручному для читання форматі.
5. Запропонувати користувачу вибрати дію:
   - Використати `prompt` для вибору дії: додавання товару, видалення товару або виведення списку.
6. Реалізувати вибір користувача:
   - Викликати відповідні функції на основі вибраної дії.
7. Повторити процес, поки користувач не завершить роботу:
   - Дати можливість користувачу виконувати дії у циклі.
*/

// початковий масив для зберігання списку покупок
let shoppingList = ["Автомобіль", "Сукня", "Кросівки"];

// функція для додавання товару до списку
function addItem(item) {
   shoppingList.push(item);
   console.log(`${item} додано до списку.`);
}

// функція для видалення товару із списку покупок
function removeItem(item) {
   const index = shoppingList.indexOf(item);
   if (index !== -1) {
      shoppingList.splice(index, 1);
      console.log(`${item} видалено зі списку.`);
   } else {
      console.log(`${item} не знайдено у списку.`);
   }
}

// функція для виведення поточного списку покупок
function displayList() {
   console.log("Поточний список покупок:");
   shoppingList.forEach((item, index) => {
      console.log(`${index + 1}. ${item}`);
   });
}

// основна функція для взаємодії з користувачем
function shoppingListManager() {
   let action;
   do {
      action = prompt("Виберіть дію: додати(add), видалити(remove), показати(show) або завершити(exit):").toLowerCase();
      switch (action) {
         case "add":
            const addItemName = prompt("Введіть назву товару для додавання:");
            addItem(addItemName);
            break;
         case "remove":
            const removeItemName = prompt("Введіть назву товару для видалення:");
            removeItem(removeItemName);
            break;
         case "show":
            displayList();
            break;
         case "exit":
            console.log("Завершення роботи.");
            break;
         default:
            console.log("Невідома дія. Спробуйте ще раз.");

      }
   } while (action !== "exit");
}

// запуск менеджера списку покупок
shoppingListManager();





























